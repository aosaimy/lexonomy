doI18n = () => {
  $("[i18n]").each(function (x) {
    const t = $(this).text();
    const h = $(this).html();
    if (Screenful.Loc[t]) {
      $(this).text(Screenful.Loc[t]);
    } else if (Screenful.Loc[h]) {
      $(this).html(Screenful.Loc[h]);
    } else {
      console.log({ notFoundLoc: t });
      notFoundLoc[t] = '';
    }
  });
  $("[i18nh]").each(function (x) {
    const h = $(this).html();
    if (Screenful.Loc[h]) {
      $(this).html(Screenful.Loc[h]);
    } else {
      console.log({notFoundLoc: h})
      notFoundLoc[h] = '';
    }
  });
};
Screenful.loc = (t) => {
  if (Screenful.Loc[t]) {
    return Screenful.Loc[t];
  } else {
    notFoundLoc[t] = '';
    return t;
  }
}